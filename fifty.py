import numpy as np
# Create a 1D NumPy array
array_1d = np.array([1, 2, 3, 4, 5])
# Compute the sum of all elements
sum_of_elements = np.sum(array_1d)
print("Sum of all elements:", sum_of_elements)
# Mean and standard deviation
mean_of_elements = np.mean(array_1d)
std_deviation = np.std(array_1d)
print("Mean of elements:", mean_of_elements)
print("Standard Deviation:", std_deviation)
# Multiply each element by 2
multiplied_array = array_1d * 2
print("Array with elements multiplied by 2:", multiplied_array)

